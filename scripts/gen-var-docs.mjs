/**
 * Var Docs Generator for @lowdefy/modules-mongodb
 *
 * Reads each module's `module.lowdefy.yaml` manifest and generates
 * `docs/{module}/reference/vars.md` — a structured Markdown reference
 * table derived from the manifest's `vars:` section. Flat vars render as
 * a single lean table; object vars with `properties:` each get a nested
 * sub-section with their own table so deep structures (workflows, layout)
 * stay readable.
 *
 * Usage:
 *   node scripts/gen-var-docs.mjs              # write all vars.md files
 *   node scripts/gen-var-docs.mjs --check      # diff only; exit 1 if stale
 *   node scripts/gen-var-docs.mjs --module layout  # single module (write mode)
 */

import {
  readFileSync,
  writeFileSync,
  existsSync,
  readdirSync,
  mkdirSync,
} from "fs";
import { join, dirname, resolve } from "path";
import { fileURLToPath } from "url";
import yaml from "js-yaml";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = resolve(__dirname, "..");
const MODULES_DIR = join(ROOT, "modules");
const DOCS_DIR = join(ROOT, "docs");

// ---------------------------------------------------------------------------
// CLI flags
// ---------------------------------------------------------------------------

const args = process.argv.slice(2);
const FLAG_CHECK = args.includes("--check");
const moduleArg = (() => {
  const idx = args.indexOf("--module");
  return idx !== -1 ? args[idx + 1] : null;
})();

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

const GENERATED_MARKER =
  "<!-- GENERATED by scripts/gen-var-docs.mjs — do not edit by hand -->";

function escapeCell(value) {
  if (value === undefined || value === null) return "";
  const str = String(value);
  // Escape pipe chars so they don't break markdown tables
  return str.replace(/\|/g, "\\|").replace(/\n/g, " ");
}

/**
 * Format a default value for display. Objects/arrays rendered as JSON;
 * strings rendered verbatim; booleans/numbers as-is.
 */
function formatDefault(value) {
  if (value === undefined || value === null) return "";
  if (typeof value === "object") {
    const json = JSON.stringify(value);
    // Keep short JSON inline; long ones get a code fence placeholder
    if (json.length <= 80) return `\`${json}\``;
    return "_(see manifest)_";
  }
  return `\`${value}\``;
}

/**
 * Build the markdown table rows for a flat map of var entries.
 * Each entry: { name, varDef } where varDef is the manifest var object.
 */
function buildFlatTable(entries) {
  const rows = entries.map(({ name, varDef }) => {
    const type = escapeCell(varDef.type ?? "");
    const defaultVal = escapeCell(formatDefault(varDef.default));
    const required = varDef.required ? "Yes" : "";
    const enumVals = Array.isArray(varDef.enum)
      ? varDef.enum.map((v) => `\`${v}\``).join(", ")
      : "";
    const desc = escapeCell(
      [varDef.description ?? "", enumVals ? `Enum: ${enumVals}` : ""]
        .filter(Boolean)
        .join(" "),
    );
    return `| \`${name}\` | ${type} | ${defaultVal} | ${required} | ${desc} |`;
  });

  return [
    "| Name | Type | Default | Required | Description |",
    "|---|---|---|---|---|",
    ...rows,
  ].join("\n");
}

/**
 * Generate the full markdown body for a module's vars section.
 * @param {string} moduleName — manifest `name` field (e.g. "Workflows")
 * @param {Record<string, object>} vars — parsed vars block from manifest
 * @returns {string} markdown body (without front-matter)
 */
function buildVarsMarkdown(moduleName, vars) {
  const lines = [];
  lines.push(GENERATED_MARKER);
  lines.push("");
  lines.push(`# ${moduleName} — Vars`);
  lines.push("");
  lines.push(
    "Var definitions are derived from `module.lowdefy.yaml`. Pass these via the `vars:` block in your app's `lowdefy.yaml` entry for this module.",
  );
  lines.push("");

  // Separate flat vars from object vars that have nested properties
  const flatEntries = [];
  const nestedEntries = [];

  for (const [name, varDef] of Object.entries(vars)) {
    if (
      varDef.type === "object" &&
      varDef.properties &&
      Object.keys(varDef.properties).length > 0
    ) {
      nestedEntries.push({ name, varDef });
    } else {
      flatEntries.push({ name, varDef });
    }
  }

  // --- Top-level summary table (ALL vars, flat and object alike) ----------
  const allEntries = [...flatEntries, ...nestedEntries];
  lines.push("## Summary");
  lines.push("");
  lines.push(buildFlatTable(allEntries));
  lines.push("");

  // --- Nested sub-sections for object vars with properties ----------------
  if (nestedEntries.length > 0) {
    lines.push("## Nested var details");
    lines.push("");

    for (const { name, varDef } of nestedEntries) {
      lines.push(`### \`${name}\``);
      lines.push("");
      if (varDef.description) {
        lines.push(varDef.description.trim());
        lines.push("");
      }
      // Property sub-table
      const propEntries = Object.entries(varDef.properties).map(
        ([propName, propDef]) => ({
          name: propName,
          varDef: propDef,
        }),
      );
      if (propEntries.length > 0) {
        lines.push(buildFlatTable(propEntries));
        lines.push("");
      }
    }
  }

  return lines.join("\n").trimEnd() + "\n";
}

/**
 * Build the full file content (front-matter + body) for vars.md.
 * @param {string} moduleSlug — directory name / lowercase slug (e.g. "user-admin")
 * @param {string} manifestName — human-readable name from manifest (e.g. "User Admin")
 * @param {Record<string, object>} vars — parsed vars block from manifest
 */
function buildVarsFile(moduleSlug, manifestName, vars) {
  const frontMatter = [
    "---",
    `title: Vars`,
    `module: ${moduleSlug}`,
    `type: reference`,
    "---",
    "",
  ].join("\n");

  return frontMatter + buildVarsMarkdown(manifestName, vars);
}

// ---------------------------------------------------------------------------
// Module discovery
// ---------------------------------------------------------------------------

function discoverModules() {
  const entries = readdirSync(MODULES_DIR, { withFileTypes: true });
  return entries
    .filter(
      (e) =>
        e.isDirectory() &&
        existsSync(join(MODULES_DIR, e.name, "module.lowdefy.yaml")),
    )
    .map((e) => e.name)
    .sort();
}

function readManifest(moduleName) {
  const manifestPath = join(MODULES_DIR, moduleName, "module.lowdefy.yaml");
  if (!existsSync(manifestPath)) return null;
  const raw = readFileSync(manifestPath, "utf-8");
  try {
    return yaml.load(raw);
  } catch (err) {
    console.warn(
      `WARN: ${moduleName} — failed to parse manifest (${manifestPath}): ${err.message}`,
    );
    return null;
  }
}

// ---------------------------------------------------------------------------
// Write mode
// ---------------------------------------------------------------------------

function generateForModule(moduleName) {
  const manifest = readManifest(moduleName);
  if (!manifest || !manifest.vars || Object.keys(manifest.vars).length === 0) {
    return null; // No vars to document
  }

  const content = buildVarsFile(
    moduleName,
    manifest.name ?? moduleName,
    manifest.vars,
  );
  return content;
}

function writeMode(modules) {
  let written = 0;
  let skipped = 0;

  for (const mod of modules) {
    const content = generateForModule(mod);
    if (content === null) {
      console.log(`  ${mod}: no vars — skipped`);
      skipped++;
      continue;
    }

    const outDir = join(DOCS_DIR, mod, "reference");
    const outPath = join(outDir, "vars.md");
    mkdirSync(outDir, { recursive: true });
    writeFileSync(outPath, content, "utf-8");
    console.log(`  ${mod}: wrote ${outPath}`);
    written++;
  }

  return { written, skipped };
}

// ---------------------------------------------------------------------------
// Check mode
// ---------------------------------------------------------------------------

function checkMode(modules) {
  const drifted = [];

  for (const mod of modules) {
    const content = generateForModule(mod);
    const outPath = join(DOCS_DIR, mod, "reference", "vars.md");

    if (content === null) {
      // No vars — committed file should not exist
      if (existsSync(outPath)) {
        console.error(
          `  DRIFT: ${mod} — vars.md exists but manifest has no vars (should be deleted)`,
        );
        drifted.push(mod);
      }
      continue;
    }

    if (!existsSync(outPath)) {
      console.error(
        `  DRIFT: ${mod} — vars.md missing (run gen-var-docs.mjs to create it)`,
      );
      drifted.push(mod);
      continue;
    }

    const committed = readFileSync(outPath, "utf-8");
    if (committed !== content) {
      console.error(
        `  DRIFT: ${mod} — vars.md is out of date with module.lowdefy.yaml (run gen-var-docs.mjs to update it)`,
      );
      drifted.push(mod);
    }
  }

  return drifted;
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main() {
  const allModules = discoverModules();
  const modules = moduleArg
    ? allModules.filter((m) => m === moduleArg)
    : allModules;

  if (moduleArg && modules.length === 0) {
    console.error(`Module '${moduleArg}' not found in ${MODULES_DIR}`);
    process.exit(1);
  }

  if (FLAG_CHECK) {
    console.log("Var Docs Generator — check mode\n");
    const drifted = checkMode(modules);
    if (drifted.length === 0) {
      console.log(`All vars.md files are up to date.`);
      process.exit(0);
    } else {
      console.error(
        `\n${drifted.length} module(s) have stale vars.md: ${drifted.join(", ")}`,
      );
      console.error("Run `node scripts/gen-var-docs.mjs` to regenerate.");
      process.exit(1);
    }
  } else {
    console.log("Var Docs Generator\n");
    const { written, skipped } = writeMode(modules);
    console.log(`\nDone: ${written} written, ${skipped} skipped (no vars).`);
  }
}

main();
